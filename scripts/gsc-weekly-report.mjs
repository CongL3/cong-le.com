#!/usr/bin/env node
/**
 * gsc-weekly-report.mjs
 * Pulls Google Search Console data for sc-domain:cong-le.com and writes a
 * markdown report to seo-reports/gsc/<date>.md (+ latest.md). No dependencies:
 * service-account JWT is signed with node:crypto.
 *
 * Auth: GSC_SA_KEY env var = service-account JSON (gsc-reporter@identifyanything,
 * delegated owner of the domain property). Optional NTFY_TOPIC env var sends a
 * push summary via ntfy.sh.
 */

import { createSign } from 'crypto';
import { writeFileSync, mkdirSync, readFileSync, existsSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const SITE = 'sc-domain:cong-le.com';
const REPORT_DIR = path.join(ROOT, 'seo-reports', 'gsc');

function b64url(buf) {
  return Buffer.from(buf).toString('base64url');
}

async function getAccessToken(sa) {
  const now = Math.floor(Date.now() / 1000);
  const header = b64url(JSON.stringify({ alg: 'RS256', typ: 'JWT' }));
  const claims = b64url(JSON.stringify({
    iss: sa.client_email,
    scope: 'https://www.googleapis.com/auth/webmasters.readonly',
    aud: 'https://oauth2.googleapis.com/token',
    iat: now,
    exp: now + 3600,
  }));
  const signer = createSign('RSA-SHA256');
  signer.update(`${header}.${claims}`);
  const sig = signer.sign(sa.private_key).toString('base64url');
  const res = await fetch('https://oauth2.googleapis.com/token', {
    method: 'POST',
    headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
    body: `grant_type=urn:ietf:params:oauth:grant-type:jwt-bearer&assertion=${header}.${claims}.${sig}`,
  });
  if (!res.ok) throw new Error(`token exchange failed: ${res.status} ${await res.text()}`);
  return (await res.json()).access_token;
}

async function query(token, body) {
  const res = await fetch(
    `https://www.googleapis.com/webmasters/v3/sites/${encodeURIComponent(SITE)}/searchAnalytics/query`,
    {
      method: 'POST',
      headers: { Authorization: `Bearer ${token}`, 'Content-Type': 'application/json' },
      body: JSON.stringify(body),
    }
  );
  if (!res.ok) throw new Error(`query failed: ${res.status} ${await res.text()}`);
  return (await res.json()).rows || [];
}

function dstr(d) {
  return d.toISOString().slice(0, 10);
}

function table(rows, label) {
  if (!rows.length) return `_No ${label} data yet._\n`;
  const lines = [`| ${label} | Clicks | Impressions | CTR | Position |`, '|---|---|---|---|---|'];
  for (const r of rows) {
    lines.push(
      `| ${r.keys[0].replace(/\|/g, '\\|').slice(0, 80)} | ${r.clicks} | ${r.impressions} | ${(r.ctr * 100).toFixed(1)}% | ${r.position.toFixed(1)} |`
    );
  }
  return lines.join('\n') + '\n';
}

function sum(rows, k) {
  return rows.reduce((a, r) => a + r[k], 0);
}

async function main() {
  const sa = JSON.parse(process.env.GSC_SA_KEY || readFileSync(process.env.GSC_SA_KEY_FILE, 'utf8'));
  const token = await getAccessToken(sa);

  const end = new Date(Date.now() - 2 * 86400 * 1000); // GSC data lags ~2 days
  const start = new Date(end.getTime() - 6 * 86400 * 1000); // 7-day window
  const prevEnd = new Date(start.getTime() - 86400 * 1000);
  const prevStart = new Date(prevEnd.getTime() - 6 * 86400 * 1000);
  const range = { startDate: dstr(start), endDate: dstr(end) };

  const [daily, prevDaily, topQueries, topPages, countries] = await Promise.all([
    query(token, { ...range, dimensions: ['date'] }),
    query(token, { startDate: dstr(prevStart), endDate: dstr(prevEnd), dimensions: ['date'] }),
    query(token, { ...range, dimensions: ['query'], rowLimit: 15 }),
    query(token, { ...range, dimensions: ['page'], rowLimit: 15 }),
    query(token, { ...range, dimensions: ['country'], rowLimit: 8 }),
  ]);

  const clicks = sum(daily, 'clicks');
  const impressions = sum(daily, 'impressions');
  const prevClicks = sum(prevDaily, 'clicks');
  const prevImpressions = sum(prevDaily, 'impressions');
  const delta = (cur, prev) => (prev === 0 ? (cur > 0 ? '(new)' : '') : `(${cur >= prev ? '+' : ''}${(((cur - prev) / prev) * 100).toFixed(0)}% wow)`);

  const today = dstr(new Date());
  const md = `# Search Console weekly report — ${today}

Window: ${range.startDate} → ${range.endDate} (GSC data lags ~2 days). Property: ${SITE}.

## Totals
- **Clicks: ${clicks}** ${delta(clicks, prevClicks)}
- **Impressions: ${impressions}** ${delta(impressions, prevImpressions)}

## Daily trend
${table(daily, 'Date')}
## Top queries
${table(topQueries, 'Query')}
## Top pages
${table(topPages, 'Page')}
## Countries
${table(countries, 'Country')}
---
Generated by .github/workflows/gsc-report.yml. Interpretation guide: /Users/congle/Dev/SEO/PLAYBOOK.md §Measurement (repo readers: see TASKS/PLAYBOOK in the SEO workspace).
`;

  mkdirSync(REPORT_DIR, { recursive: true });
  writeFileSync(path.join(REPORT_DIR, `${today}.md`), md);
  writeFileSync(path.join(REPORT_DIR, 'latest.md'), md);
  console.log(`report written: seo-reports/gsc/${today}.md (clicks ${clicks}, impressions ${impressions})`);

  if (process.env.NTFY_TOPIC) {
    const headline = `SEO week: ${clicks} clicks ${delta(clicks, prevClicks)}, ${impressions} impressions ${delta(impressions, prevImpressions)} — top: ${topQueries[0]?.keys[0] || 'no queries yet'}`;
    try {
      await fetch(`https://ntfy.sh/${process.env.NTFY_TOPIC}`, {
        method: 'POST',
        headers: { Title: 'cong-le.com search report', Tags: 'chart_with_upwards_trend' },
        body: headline,
      });
      console.log('ntfy push sent');
    } catch (e) {
      console.warn(`ntfy push failed (non-fatal): ${e.message}`);
    }
  }
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
