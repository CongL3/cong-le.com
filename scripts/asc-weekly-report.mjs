#!/usr/bin/env node
/**
 * asc-weekly-report.mjs
 * Pulls App Store Connect analytics reports for all apps on the account and
 * writes a markdown section to seo-reports/asc/<date>.md (+ latest.md), plus a
 * machine-readable summary.json the GSC report reads to append store numbers to
 * its ntfy push. No dependencies: the ES256 JWT is signed with node:crypto.
 *
 * Auth (App Store Connect API key, ES256):
 *   ASC_P8       — contents of the .p8 private key (CI secret), OR
 *   ASC_P8_FILE  — path to the .p8 file (local testing fallback)
 *   ASC_KEY_ID   — key id (e.g. 4C27Y3D52G)
 *   ASC_ISSUER_ID
 *
 * Reports are generated by an ONGOING analyticsReportRequest per app. Apple only
 * produces the first data instances ~24-48h after the request is created, so a
 * freshly-set-up account yields zero instances — this script handles that
 * gracefully and emits a "pending" report instead of failing.
 *
 * The app -> reportRequest -> report-id mapping is cached in
 * seo-reports/asc/report-map.json (committed) so weekly runs skip rediscovery;
 * missing entries are rebuilt lazily.
 */

import { createSign, createPrivateKey } from 'crypto';
import { gunzipSync } from 'zlib';
import { writeFileSync, mkdirSync, readFileSync, existsSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const REPORT_DIR = path.join(ROOT, 'seo-reports', 'asc');
const MAP_FILE = path.join(REPORT_DIR, 'report-map.json');
const BASE = 'https://api.appstoreconnect.apple.com';
const CONCURRENCY = 8;
const WINDOW_DAYS = 7;

// Report categories we care about, keyed by our internal name. `names` is an
// ordered preference list matched case-insensitively against the live report
// list (first match wins). "Detailed" variants carry the source/referrer
// dimension we need for web-referrer attribution.
const TARGETS = {
  discovery: { names: ['App Store Discovery and Engagement Detailed', 'App Store Discovery and Engagement Standard'] },
  downloads: { names: ['App Downloads Standard', 'App Downloads Detailed'] },
  sessions: { names: ['App Sessions Standard', 'App Sessions Detailed'] },
  crashes: { names: ['App Crashes'] },
  purchases: { names: ['App Store Purchases Standard', 'App Store Purchases Detailed'] },
};

// ---------------------------------------------------------------- auth --------

function loadKey() {
  if (process.env.ASC_P8) return process.env.ASC_P8;
  if (process.env.ASC_P8_FILE) return readFileSync(process.env.ASC_P8_FILE, 'utf8');
  throw new Error('missing ASC_P8 or ASC_P8_FILE');
}

function ascToken() {
  const key = createPrivateKey(loadKey());
  const now = Math.floor(Date.now() / 1000);
  const b64 = (o) => Buffer.from(JSON.stringify(o)).toString('base64url');
  const h = b64({ alg: 'ES256', kid: process.env.ASC_KEY_ID, typ: 'JWT' });
  const p = b64({ iss: process.env.ASC_ISSUER_ID, iat: now, exp: now + 1200, aud: 'appstoreconnect-v1' });
  const s = createSign('SHA256');
  s.update(`${h}.${p}`);
  return `${h}.${p}.${s.sign({ key, dsaEncoding: 'ieee-p1363' }).toString('base64url')}`;
}

// ---------------------------------------------------------------- http --------

let TOKEN = '';

async function api(pathOrUrl, { retries = 3 } = {}) {
  const url = pathOrUrl.startsWith('http') ? pathOrUrl : BASE + pathOrUrl;
  for (let attempt = 0; ; attempt++) {
    const res = await fetch(url, { headers: { Authorization: `Bearer ${TOKEN}` } });
    if (res.ok) return res.json();
    // 429 / 5xx: back off and retry; other 4xx: fail fast
    if ((res.status === 429 || res.status >= 500) && attempt < retries) {
      await sleep(500 * (attempt + 1));
      continue;
    }
    throw new Error(`${res.status} ${url}: ${(await res.text()).slice(0, 300)}`);
  }
}

// Follow JSON:API pagination, concatenating `.data`.
async function apiAll(pathOrUrl) {
  let url = pathOrUrl;
  const out = [];
  while (url) {
    const page = await api(url);
    out.push(...(page.data || []));
    url = page.links?.next || null;
  }
  return out;
}

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

// Run `fn` over `items` with a bounded pool; collects results in order.
async function pool(items, size, fn) {
  const results = new Array(items.length);
  let i = 0;
  async function worker() {
    while (i < items.length) {
      const idx = i++;
      results[idx] = await fn(items[idx], idx);
    }
  }
  await Promise.all(Array.from({ length: Math.min(size, items.length) }, worker));
  return results;
}

// ----------------------------------------------------------- discovery --------

async function listApps() {
  const apps = await apiAll('/v1/apps?fields[apps]=name&limit=200');
  return apps.map((a) => ({ id: a.id, name: a.attributes?.name || a.id }));
}

// Pick the report id matching a target's preference list.
function pickReport(reports, names) {
  for (const want of names) {
    const hit = reports.find((r) => (r.attributes?.name || '').toLowerCase() === want.toLowerCase());
    if (hit) return { id: hit.id, name: hit.attributes.name };
  }
  return null;
}

// Resolve (and cache) requestId + per-target report ids for one app.
async function resolveApp(app, cached) {
  const entry = cached && cached.reports ? { ...cached } : { name: app.name, reports: {} };
  entry.name = app.name;
  const need = Object.keys(TARGETS).filter((k) => !entry.reports[k]);
  if (entry.requestId && need.length === 0) return entry;

  if (!entry.requestId) {
    const reqs = await apiAll(`/v1/apps/${app.id}/analyticsReportRequests?filter[accessType]=ONGOING`);
    const req = reqs.find((r) => !r.attributes?.stoppedDueToInactivity) || reqs[0];
    if (!req) throw new Error('no ONGOING analyticsReportRequest');
    entry.requestId = req.id;
  }

  const reports = await apiAll(`/v1/analyticsReportRequests/${entry.requestId}/reports?limit=200`);
  for (const [key, t] of Object.entries(TARGETS)) {
    if (entry.reports[key]) continue;
    const picked = pickReport(reports, t.names);
    if (picked) entry.reports[key] = picked.id;
  }
  return entry;
}

// ------------------------------------------------------------ instances -------

// Most recent DAILY instances (up to `n`), newest first.
async function recentInstances(reportId, n) {
  const inst = await apiAll(`/v1/analyticsReports/${reportId}/instances?filter[granularity]=DAILY&limit=200`);
  inst.sort((a, b) => (b.attributes?.processingDate || '').localeCompare(a.attributes?.processingDate || ''));
  return inst.slice(0, n);
}

// Download + parse every segment of an instance into an array of row objects.
async function instanceRows(instanceId) {
  const segments = await apiAll(`/v1/analyticsReportInstances/${instanceId}/segments`);
  const rows = [];
  for (const seg of segments) {
    const url = seg.attributes?.url;
    if (!url) continue;
    const res = await fetch(url); // pre-signed URL, no auth header
    if (!res.ok) throw new Error(`segment download ${res.status}`);
    let buf = Buffer.from(await res.arrayBuffer());
    if (buf[0] === 0x1f && buf[1] === 0x8b) buf = gunzipSync(buf); // gzip magic
    rows.push(...parseTsv(buf.toString('utf8')));
  }
  return rows;
}

// Apple analytics report bodies are tab-separated with a header row.
function parseTsv(text) {
  const lines = text.split(/\r?\n/).filter((l) => l.length);
  if (!lines.length) return [];
  const header = lines[0].split('\t').map((h) => h.trim());
  return lines.slice(1).map((line) => {
    const cells = line.split('\t');
    const row = {};
    header.forEach((h, i) => (row[h] = cells[i]));
    return row;
  });
}

// --------------------------------------------------------- aggregation --------

// Column-name matching is done by substring so the code survives minor schema
// differences between Standard/Detailed variants.
function findCol(row, ...regexes) {
  const keys = Object.keys(row);
  for (const re of regexes) {
    const k = keys.find((c) => re.test(c));
    if (k) return k;
  }
  return null;
}

function num(v) {
  if (v == null) return 0;
  const n = Number(String(v).replace(/[^0-9.\-]/g, ''));
  return Number.isFinite(n) ? n : 0;
}

function sumCol(rows, col, filter) {
  if (!col) return 0;
  let total = 0;
  for (const r of rows) {
    if (filter && !filter(r)) continue;
    total += num(r[col]);
  }
  return total;
}

// Extract the metrics we report for one app from its per-category row sets.
function aggregate(byCat) {
  const out = {
    impressions: 0, pageViews: 0, downloads: 0, redownloads: 0,
    sessions: 0, crashes: 0, proceeds: 0, purchases: 0, webReferrer: {},
  };

  const disc = byCat.discovery || [];
  if (disc.length) {
    const c = disc[0];
    const impCol = findCol(c, /^impressions$/i, /impressions/i);
    const pvCol = findCol(c, /product page view/i, /^page views$/i, /page view/i);
    const countCol = findCol(c, /^counts$/i, /^count$/i);
    const eventCol = findCol(c, /^engagement type$/i, /^event$/i, /^event type$/i);
    const srcCol = findCol(c, /^source type$/i, /source type/i, /^source$/i);
    if (impCol) out.impressions = sumCol(disc, impCol);
    if (pvCol) out.pageViews = sumCol(disc, pvCol);
    // Fallback: some variants encode metric via an Event dimension + Counts.
    if (!impCol && eventCol && countCol) out.impressions = sumCol(disc, countCol, (r) => /impression/i.test(r[eventCol]));
    if (!pvCol && eventCol && countCol) out.pageViews = sumCol(disc, countCol, (r) => /page view/i.test(r[eventCol]));
    // Web-referrer breakout: rows whose source dimension is a web referrer.
    if (srcCol) {
      const metricCol = pvCol || impCol || countCol;
      for (const r of disc) {
        if (!/web/i.test(r[srcCol] || '')) continue;
        const label = (r[findCol(r, /source info/i, /referrer/i, /domain/i)] || r[srcCol] || 'web').toString();
        out.webReferrer[label] = (out.webReferrer[label] || 0) + num(r[metricCol]);
      }
    }
  }

  const dl = byCat.downloads || [];
  if (dl.length) {
    const c = dl[0];
    const typeCol = findCol(c, /download type/i);
    const countCol = findCol(c, /^counts$/i, /^downloads$/i, /^count$/i);
    if (countCol) {
      out.downloads = sumCol(dl, countCol, typeCol ? (r) => /first/i.test(r[typeCol]) : null);
      if (typeCol) out.redownloads = sumCol(dl, countCol, (r) => /re-?download/i.test(r[typeCol]));
      if (!typeCol) out.downloads = sumCol(dl, countCol);
    }
  }

  const ses = byCat.sessions || [];
  if (ses.length) {
    const c = ses[0];
    const col = findCol(c, /^sessions$/i, /session count/i, /^counts$/i);
    out.sessions = sumCol(ses, col);
  }

  const cr = byCat.crashes || [];
  if (cr.length) {
    const c = cr[0];
    const col = findCol(c, /^crashes$/i, /crash count/i, /^counts$/i);
    out.crashes = sumCol(cr, col);
  }

  const pur = byCat.purchases || [];
  if (pur.length) {
    const c = pur[0];
    const proceedsCol = findCol(c, /proceeds/i, /amount/i, /revenue/i);
    const purCol = findCol(c, /^purchases$/i, /^units$/i, /^quantity$/i, /^counts$/i);
    out.proceeds = sumCol(pur, proceedsCol);
    out.purchases = sumCol(pur, purCol);
  }

  return out;
}

// -------------------------------------------------------------- per app -------

async function processApp(app, cached) {
  const entry = await resolveApp(app, cached);
  const byCat = {};
  let instanceCount = 0;
  for (const [key] of Object.entries(TARGETS)) {
    const reportId = entry.reports[key];
    if (!reportId) continue;
    const instances = await recentInstances(reportId, WINDOW_DAYS);
    instanceCount += instances.length;
    const rows = [];
    for (const inst of instances) rows.push(...(await instanceRows(inst.id)));
    byCat[key] = rows;
  }
  const metrics = aggregate(byCat);
  return { app, entry, metrics, instanceCount };
}

// -------------------------------------------------------------- render --------

function dstr(d) {
  return d.toISOString().slice(0, 10);
}

function isZero(m) {
  return !(m.impressions || m.pageViews || m.downloads || m.redownloads || m.sessions || m.crashes || m.proceeds || m.purchases);
}

function fmt(n) {
  return Number.isInteger(n) ? n.toLocaleString('en-US') : n.toFixed(2);
}

function render(results, { today, anyInstances }) {
  const active = results.filter((r) => r && !isZero(r.metrics));
  const zeroCount = results.filter((r) => r && isZero(r.metrics)).length;
  const failed = results.filter((r) => !r);
  active.sort((a, b) => b.metrics.downloads - a.metrics.downloads);

  const totals = results.reduce((t, r) => {
    if (!r) return t;
    const m = r.metrics;
    t.impressions += m.impressions; t.pageViews += m.pageViews; t.downloads += m.downloads;
    t.redownloads += m.redownloads; t.sessions += m.sessions; t.crashes += m.crashes;
    t.proceeds += m.proceeds; t.purchases += m.purchases;
    return t;
  }, { impressions: 0, pageViews: 0, downloads: 0, redownloads: 0, sessions: 0, crashes: 0, proceeds: 0, purchases: 0 });

  const lines = [`# App Store Connect weekly report — ${today}`, ''];
  lines.push(`Window: most recent ${WINDOW_DAYS} full days of data per app. ${results.filter(Boolean).length}/${results.length} apps resolved.`, '');

  if (!anyInstances) {
    lines.push('> **Status: pending.** Analytics report requests were created but Apple has not produced any data instances yet. Apple generates the first data ~24-48h after setup, so the first non-empty report lands next run. Report IDs were discovered successfully (see report-map.json), which confirms the API plumbing works.', '');
  }

  lines.push('## Totals');
  lines.push(`- Impressions: **${fmt(totals.impressions)}** · Product page views: **${fmt(totals.pageViews)}**`);
  lines.push(`- Downloads: **${fmt(totals.downloads)}** (redownloads ${fmt(totals.redownloads)}) · Conversion: **${pct(totals.downloads, totals.pageViews)}**`);
  lines.push(`- Sessions: **${fmt(totals.sessions)}** · Crashes: **${fmt(totals.crashes)}**`);
  lines.push(`- Purchases: **${fmt(totals.purchases)}** · Proceeds: **$${fmt(totals.proceeds)}**`);
  lines.push('');

  lines.push('## Apps by downloads');
  if (active.length) {
    lines.push('| App | Impr. | Page views | Downloads | Conv. | Sessions | Crashes | Proceeds |', '|---|--:|--:|--:|--:|--:|--:|--:|');
    for (const r of active) {
      const m = r.metrics;
      lines.push(`| ${esc(r.app.name)} | ${fmt(m.impressions)} | ${fmt(m.pageViews)} | ${fmt(m.downloads)} | ${pct(m.downloads, m.pageViews)} | ${fmt(m.sessions)} | ${fmt(m.crashes)} | $${fmt(m.proceeds)} |`);
    }
  } else {
    lines.push('_No app-level activity in the reporting window yet._');
  }
  lines.push('');
  if (zeroCount) lines.push(`…${zeroCount} apps with no activity.`, '');
  if (failed.length) lines.push(`> ${failed.length} apps failed to resolve and were skipped (see run log).`, '');

  // Web referrers subsection
  const refRows = [];
  for (const r of results) {
    if (!r) continue;
    for (const [label, count] of Object.entries(r.metrics.webReferrer)) {
      if (count > 0) refRows.push({ app: r.app.name, label, count });
    }
  }
  if (refRows.length) {
    refRows.sort((a, b) => b.count - a.count);
    lines.push('## Web referrers', '', '| App | Referrer | Views |', '|---|---|--:|');
    for (const r of refRows.slice(0, 40)) lines.push(`| ${esc(r.app)} | ${esc(r.label)} | ${fmt(r.count)} |`);
    lines.push('', '_Web-referrer rows carry `ct=`/`pt=` campaign attribution from the Discovery & Engagement report._', '');
  }

  lines.push('---', `Generated by .github/workflows/gsc-report.yml (asc-weekly-report.mjs). Interpretation: /Users/congle/Dev/SEO/PLAYBOOK.md §Measurement.`);
  return { md: lines.join('\n') + '\n', totals, topApp: active[0]?.app.name || null };
}

function pct(a, b) {
  return b > 0 ? `${((a / b) * 100).toFixed(1)}%` : '—';
}
function esc(s) {
  return String(s).replace(/\|/g, '\\|').slice(0, 60);
}

// ---------------------------------------------------------------- main --------

async function main() {
  TOKEN = ascToken();
  mkdirSync(REPORT_DIR, { recursive: true });

  let map = {};
  if (existsSync(MAP_FILE)) {
    try { map = JSON.parse(readFileSync(MAP_FILE, 'utf8')).apps || {}; } catch { map = {}; }
  }

  const apps = await listApps();
  console.log(`resolved ${apps.length} apps`);

  const results = await pool(apps, CONCURRENCY, async (app) => {
    try {
      const r = await processApp(app, map[app.id]);
      map[app.id] = r.entry; // refresh cache
      return r;
    } catch (e) {
      console.warn(`app ${app.name} (${app.id}) failed: ${e.message}`);
      return null;
    }
  });

  writeFileSync(MAP_FILE, JSON.stringify({ generatedAt: new Date().toISOString(), apps: map }, null, 2) + '\n');

  const anyInstances = results.some((r) => r && r.instanceCount > 0);
  const today = dstr(new Date());
  const { md, totals, topApp } = render(results, { today, anyInstances });

  writeFileSync(path.join(REPORT_DIR, `${today}.md`), md);
  writeFileSync(path.join(REPORT_DIR, 'latest.md'), md);
  writeFileSync(path.join(REPORT_DIR, 'summary.json'), JSON.stringify({
    date: today, pending: !anyInstances,
    downloads: totals.downloads, proceeds: totals.proceeds, topApp,
  }, null, 2) + '\n');

  // Report name discovery log for orchestrator sanity-checking.
  const sample = results.find((r) => r && r.entry.reports);
  if (sample) {
    console.log('discovered report ids (sample app):');
    for (const [k, id] of Object.entries(sample.entry.reports)) console.log(`  ${k}: ${id}`);
  }
  console.log(`report written: seo-reports/asc/${today}.md — downloads ${totals.downloads}, proceeds $${totals.proceeds}, pending=${!anyInstances}`);
}

main().then(() => process.exit(0)).catch((e) => {
  // Never fail the pipeline on report errors — emit and exit 0.
  console.error(`asc-weekly-report error (non-fatal): ${e.stack || e.message}`);
  process.exit(0);
});
